<p><strong>創業講座｜學而時習，翻轉創新視野｜談談教育</strong></p>
<p>&nbsp;</p>
<p><span style="font-weight: 400;">有關教育議題的討論，不管什麼時候都會受到社會大眾的格外關注。隨著講求效率的時代來臨，要如何讓學生快速將知識內化吸收，變成越來越重要的議題。不同的教學方法如雨後春筍般興起，就是為了改善傳統填鴨式教育------只會考試不會自我思考，對學生造成的致命缺陷。</span></p>
