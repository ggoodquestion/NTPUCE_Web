<p><strong>社企講座｜攜手喵星人，像浪浪不流浪｜流浪動物議題</strong></p>
<p>&nbsp;</p>
<p><span style="font-weight: 400;">在2018年台灣的流浪動物以接近15萬隻，近年來更是逐年遞增，但隨著社會愈加重視動物保護議題：如何給流浪動物一個溫暖的家，是外界一直在思考的問題。</span></p>
<p>&nbsp;</p>
<p><span style="font-weight: 400;">這次台北大學創新創業中心邀請到----拼圖喵中途之家創辦人 陳人祥</span></p>
<p><span style="font-weight: 400;">講師將分享他是如何建構一個串連小農、流浪貓咪、消費者的循環體制！他蒐集拼圖喵中途之家數百隻貓咪的貓砂，並將之堆肥後提供給合作小農，換取有機蔬菜的供應。另外，他成立品牌「養貓人家」，將有機菜製作為最適合貓咪吃的食物，賣給需要購買貓食的消費者們。透過這樣的機制，消費者的貓咪們吃到更營養的食物的同時，中途之家的貓咪們可以得到更好的照顧，讓消費者每一筆的訂單，都發揮更大效益！</span></p>
